class Users {
  final String firstName;
  final String lastName;
  final String email;
  final String studentNumber;
  final String studyYear;
  final String major;
  final String role;

  Users({
    required this.firstName,
    required this.lastName,
    required this.email,
    required this.studentNumber,
    required this.studyYear,
    required this.major,
    required this.role,
  });

}
